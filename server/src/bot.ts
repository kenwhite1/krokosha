import { Bot, webhookCallback } from 'grammy'
import { BOT_TOKEN } from './auth'
import { APP_URL } from './env'

export const bot = BOT_TOKEN ? new Bot(BOT_TOKEN) : null

const OPEN = 'Играть 🐊'

export function appKeyboard(startParam?: string) {
  if (!APP_URL) return undefined
  const url = startParam ? `${APP_URL}?startapp=${encodeURIComponent(startParam)}` : APP_URL
  return { inline_keyboard: [[{ text: OPEN, web_app: { url } }]] }
}

if (bot) {
  void bot.api.setMyDescription(
    'Крокоша это весёлая игра в ассоциации, цифровой «Крокодил».\n\n' +
      'Один игрок становится ведущим: ему выпадает тайное слово, и он по одной открывает подсказки. ' +
      'Остальные наперегонки пишут свои догадки. Кто угадал первым, забирает очки, и чем меньше подсказок ушло, тем больше награда.\n\n' +
      'Без спешки и без нервов: можно сыграть с ботами в одиночку, найти случайных соперников или позвать друзей в общую комнату по коду из четырёх букв.\n\n' +
      'Заходи, когда захочется размять воображение.',
  ).catch(() => {})
  void bot.api.setMyShortDescription('Весёлая игра в ассоциации. Угадывай слова по подсказкам с ботами или друзьями 🐊').catch(() => {})

  // Стандартное меню команд (список по «/»).
  void bot.api.setMyCommands([
    { command: 'start', description: 'Открыть игру' },
    { command: 'play', description: 'Сыграть партию' },
    { command: 'rules', description: 'Как играть' },
    { command: 'about', description: 'Об игре' },
    { command: 'help', description: 'Помощь' },
  ]).catch(() => {})

  bot.command('start', async ctx => {
    const payload = ctx.match?.toString() ?? ''
    const startParam = payload.startsWith('room_') ? payload : undefined
    const name = ctx.from?.first_name || 'друг'
    await ctx.reply(
      `Привет, ${name}! Это Крокоша 🐊\n\n` +
        'Если совсем просто, это игра в ассоциации, цифровой «Крокодил». ' +
        'Один игрок становится ведущим: ему выпадает тайное слово, и он подсказку за подсказкой намекает на него. ' +
        'Все остальные стараются угадать слово первыми.\n\n' +
        'Чем меньше подсказок понадобилось, тем больше очков получают и тот, кто угадал, и сам ведущий. ' +
        'Потом ведущим становится следующий, и так по кругу.\n\n' +
        'Ничего сложного и никакой спешки: можно сыграть с ботами в одиночку, можно найти случайных соперников, ' +
        'а можно позвать друзей в общую комнату по коду. Нажми кнопку «Играть» внизу и заходи. Жду тебя! 🐊',
      { reply_markup: appKeyboard(startParam) },
    )
  })

  bot.command('play', async ctx => {
    await ctx.reply('Загадываю слово 🐊 Заходи угадывать!', { reply_markup: appKeyboard() })
  })

  bot.command('rules', async ctx => {
    await ctx.reply(
      'Правила Крокоши 🐊\n\n' +
        'Цель: набрать больше всех очков за партию.\n\n' +
        '• Каждый раунд один игрок становится ведущим и получает тайное слово.\n' +
        '• Ведущий по одной открывает подсказки, намекая на слово.\n' +
        '• Остальные пишут догадки в поле ответа. Угадал первым, забрал очки.\n' +
        '• Чем меньше подсказок открыто к моменту догадки, тем больше очков получают и угадавший, и ведущий.\n' +
        '• Потом ведущим становится следующий игрок, и так по кругу.\n\n' +
        'Кто наберёт больше всех очков за все раунды, тот и победил.',
      { reply_markup: appKeyboard() },
    )
  })

  bot.command('about', async ctx => {
    await ctx.reply(
      'Крокоша 🐊\n\n' +
        'Тёплая игра в ассоциации по мотивам дворового «Крокодила», только своя: ' +
        'со своим словарём, уютными карточками-подсказками и сценой под софитом.\n\n' +
        'Как можно играть:\n' +
        '• Одиночная игра против ботов, три уровня сложности.\n' +
        '• Быстрая игра, случайные соперники онлайн.\n' +
        '• С друзьями, общая комната по коду из четырёх символов.\n\n' +
        'Без спешки и без нервов, на пару партий за вечер. Заходи 💚',
      { reply_markup: appKeyboard() },
    )
  })

  bot.command('help', async ctx => {
    await ctx.reply(
      'Крокоша 🐊 это игра в ассоциации: угадывай слова по подсказкам.\n\n' +
        'Команды:\n' +
        '/start - открыть игру\n' +
        '/play - сыграть партию\n' +
        '/rules - как играть\n' +
        '/about - об игре\n' +
        '/help - это сообщение\n\n' +
        'Жми кнопку ниже, чтобы играть 👇',
      { reply_markup: appKeyboard() },
    )
  })
}

export const botWebhook = bot ? webhookCallback(bot, 'hono') : null
